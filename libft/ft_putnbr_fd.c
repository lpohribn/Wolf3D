#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	int i;

	i = 0;
	if (n == -2147483648)
	{
		n = -214748364;
		i = 1;
	}
	if (n < 0 && n > -2147483648)
	{
		write(fd, "-", 1);
		n = n * (-1);
	}
	if (n > 9 && n <= 2147483647)
		ft_putnbr_fd(n / 10, fd);
	n = n % 10;
	n = n + 48;
	write(fd, &n, 1);
	if (i == 1)
	{
		write(fd, "8", 1);
	}
}
