#include <unistd.h>

void	ft_putnbr(int n)
{
	int i;

	i = 0;
	if (n == -2147483648)
	{
		n = -214748364;
		i = 1;
	}
	if (n < 0 && n > -2147483648)
	{
		write(1, "-", 1);
		n = n * (-1);
	}
	if (n > 9 && n <= 2147483647)
		ft_putnbr(n / 10);
	n = n % 10;
	n = n + 48;
	write(1, &n, 1);
	if (i == 1)
	{
		write(1, "8", 1);
	}
}
